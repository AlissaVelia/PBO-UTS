    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String tanggal = dateFormat.format(pinjam.getDate());
        String tanggal2 = dateFormat.format(kembali.getDate());
        
        if (nomor.getText().equals("") || nama.getText().equals("") || alamat.getText().equals("") || jenis.getText().equals("") || judul.getText().equals("") || pinjam.equals("") || kembali.equals("") ||
            status.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
        } 
        else 
        {
        
            String SQL = "INSERT INTO tb_peminjam (nomor,nama,alamat,jenis,judul,tglpinjam,tglkembali,status)"
                + "VALUES('"+nomor.getText()+"','"+nama.getText()+"','"+alamat.getText()
                + "','"+jenis.getText()+"','"+judul.getText()+"','"+tanggal+"','"+tanggal2+"',"
                + "'"+status.getText()+"')";
            int status = KoneksiDB.execute(SQL);
            
            if(status == 1)
            {
            JOptionPane.showMessageDialog(this, "Data Berhasil ditambahkan", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            SelectedData();
            }
            else {
                JOptionPane.showMessageDialog(this, "Data gagal ditambahkan", "Gagal",
                        JOptionPane.WARNING_MESSAGE);
            }
            
        
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int baris = tblData.getSelectedRow();
        if (baris != -1) 
        {
            String Nomor = tblData.getValueAt(baris, 0).toString();
            String SQL = "DELETE FROM tb_peminjam WHERE nomor='"+Nomor+"'";
            int status = KoneksiDB.execute(SQL);
            if (status==1) 
            {
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                
            } else {
                JOptionPane.showMessageDialog(this, "Data gagal dihapus", "Gagal", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Pilih Baris Data terlebih dahulu", "Error", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void tblDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDataMouseClicked
        // TODO add your handling code here:
        int baris = tblData.getSelectedRow();
        if (baris != 1) {
            nomor.setText(tblData.getValueAt(baris, 0).toString());
            nama.setText(tblData.getValueAt(baris, 1).toString());
            alamat.setText(tblData.getValueAt(baris, 2).toString());
            jenis.setText(tblData.getValueAt(baris, 3).toString());
            judul.setText(tblData.getValueAt(baris, 4).toString());
            
            SimpleDateFormat date = new SimpleDateFormat("yyyy-MM-dd");
            Date dateFormat = null;
            try {
                dateFormat = date.parse(tblData.getValueAt(baris, 5).toString());
                dateFormat = date.parse(tblData.getValueAt(baris, 6).toString());
            } 
            catch (ParseException ex) 
            {
                Logger.getLogger(frmMain.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            pinjam.setDate(dateFormat);
            kembali.setDate(dateFormat);
            status.setText(tblData.getValueAt(baris, 7).toString());
        }
    }//GEN-LAST:event_tblDataMouseClicked

