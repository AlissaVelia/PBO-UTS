    private void SelectedData() {
        String kolom[] = {"NOMOR","NAMA","ALAMAT","JUDUL","JENIS","PINJAM","KEMBALI","STATUS"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM tb_peminjam";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        try {
            while(rs.next()) {
                String NOMOR = rs.getString(1);
                String NAMA = rs.getString(2);
                String ALAMAT = rs.getString(3);
                String JUDUL = rs.getString(4);
                String JENIS = rs.getString(5);
                String PINJAM = rs.getString(6);
                String KEMBALI = rs.getString(7);
                String STATUS = rs.getString(8);
                String data[] = {NOMOR,NAMA,ALAMAT,JUDUL,JENIS,PINJAM,KEMBALI,STATUS};
                dtm.addRow(data);
            }
        } 
        catch (SQLException ex) {
            Logger.getLogger(frmMain.class.getName()).log(Level.SEVERE, null, ex);
        }
        tblData.setModel(dtm);
    }
